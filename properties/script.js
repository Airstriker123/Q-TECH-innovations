function changeColor() {
          
  var footerText = document.getElementById("footer-text");
  
  if (footerText.style.color === "white") {
   
    footerText.style.color = "#62ff00";
  
  } else {
  
    footerText.style.color = "white";
  
  }
}
setInterval(changeColor, 500);

